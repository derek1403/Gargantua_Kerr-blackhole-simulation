# ---------------------------------------------------------------------------
# 7. 後製模式
#    "none"  : 不做任何後製，直接輸出原始渲染結果
#    "mode1" : 均勻曝光 + Gamma + Bloom + 暖色調
#    "mode2" : 高光遮罩選擇性曝光 + Selective Bloom（更電影感）
# ---------------------------------------------------------------------------
POSTPROCESS_MODE = "mode2"

# --- Mode 1 參數 ---
MODE1_EXPOSURE  = 1.4    # 整體亮度倍率
MODE1_GAMMA     = 0.8    # Gamma 值 (<1 提亮中間調)
MODE1_CONTRAST  = 0.95   # 對比度縮放
MODE1_WARM      = 0.05   # 暖色偏移 (增紅減藍)
MODE1_BLOOM_SIGMA = 15   # Bloom 高斯模糊 sigma
MODE1_BLOOM_MIX   = 0.7  # Bloom 疊加強度

# --- Mode 2 參數 ---
MODE2_BASE_EXPOSURE  = 1.0   # 基礎亮度 (維持暗部)
MODE2_HIGH_BOOST     = 3.0   # 高光區域額外增幅倍率
MODE2_GLOW_INTENSITY = 0.8   # 高光限定 Bloom 強度
MODE2_CONTRAST       = 1.1   # 最終對比度微調
MODE2_HIGHLIGHT_POWER = 3.0  # 高光遮罩次方 (越大=越只針對最亮處)
MODE2_BLOOM_SIGMA    = 15    # Selective Bloom 高斯 sigma

# ---------------------------------------------------------------------------
# 8. 動畫參數
# ---------------------------------------------------------------------------
VIDEO_FPS          = 24    # 每秒幀數
VIDEO_DURATION_SEC = 5     # 動畫總長度 (秒)
VIDEO_OUTPUT_NAME  = "gargantua_orbit.mp4"
VIDEO_MAX_WORKERS  = 10#None  # 平行 process 數；None = 自動使用所有 CPU 核心
                           # 建議先設 CPU核心數-1，避免系統卡死，例如 7

# ---------------------------------------------------------------------------
# 9. 攝影機路徑（關鍵影格系統）
#
# 每個 keyframe 是一個 dict，包含：
#   t         : 時間 (秒)，必須從 0 開始、單調遞增、最後一個 = VIDEO_DURATION_SEC
#   azimuth   : 方位角 (度)，繞 z 軸旋轉
#   elevation : 仰角 (度)，0=赤道面，90=正上方
#   distance  : 攝影機距黑洞距離
#
# 影格之間的數值由插值函數自動補齊。
#
# CAMERA_EASING 控制插值緩動類型：
#   "ease_in_out"      : 平滑 S 曲線（最自然，推薦）
#   "critically_damped": 彈簧阻尼，略帶慣性感
#   "linear"           : 無緩動，勻速（debug 用）
# ---------------------------------------------------------------------------
CAMERA_EASING = "critically_damped"

# critically_damped 專屬參數
# omega : 自然頻率，越大收斂越快（建議範圍 3~10）
#          3 = 像在水中移動，很慵懶
#          6 = 電影攝影機標準感（預設）
#         10 = 快速跟隨，幾乎無拖曳
# 注意：critically_damped 不會過衝（不會像 ease_in_out 那樣對稱），
#       起步慢、中段加速、結尾帶慣性緩緩停下，適合模擬真實攝影機。
CAMERA_DAMPING_OMEGA = 6.0

CAMERA_KEYFRAMES = [
    # t=0s  正面靠近觀看，赤道偏上視角
    {"t": 0.0, "azimuth":   0.0, "elevation": 15.0, "distance": 50.0},

    # t=1.5s 開始向左轉並拉遠
    {"t": 1.5, "azimuth": -60.0, "elevation": 20.0, "distance": 65.0},

    # t=3.0s 繞到側面，同時拉高到俯視角度
    {"t": 3.0, "azimuth": -120.0, "elevation": 50.0, "distance": 60.0},

    # t=5.0s 幾乎正上方俯視，靜止收尾
    {"t": 5.0, "azimuth": -150.0, "elevation": 75.0, "distance": 55.0},
]
