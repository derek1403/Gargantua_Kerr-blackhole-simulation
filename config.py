# ---------------------------------------------------------------------------
# 5. 相對論效應開關
# ---------------------------------------------------------------------------
ENABLE_DOPPLER               = True   # 都卜勒頻移 (左亮右暗)
ENABLE_GRAVITATIONAL_REDSHIFT = True  # 引力時間膨脹 u^t 修正
ENABLE_BACKGROUND_IMAGE      = True   # 使用星空背景圖；False 則用純黑背景

# ---------------------------------------------------------------------------
# 6. 吸積盤細節開關
# ---------------------------------------------------------------------------
ENABLE_DISK_GAPS    = True   # 主要間隙 (土星環效果)
