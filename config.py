CAM_ELEVATION_DEG = 40.0   # 仰角 (度)，0=赤道面，90=正上方
CAM_AZIMUTH_DEG   = -20.0  # 方位角 (度)，單幀預覽用；動畫時由 video.py 覆蓋
FOV_SCALE         = 9.0    # 視野縮放
FOCAL_LENGTH      = 15.0   # 等效焦距

# ---------------------------------------------------------------------------
# 4. 渲染解析度
# ---------------------------------------------------------------------------
RENDER_RESOLUTION = 500    # 單幀解析度 (pixel)
